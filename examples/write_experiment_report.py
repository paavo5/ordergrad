#!/usr/bin/env python3
from __future__ import annotations

import argparse
from pathlib import Path


def latex_escape(text: str) -> str:
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
    }
    out = text
    for k, v in replacements.items():
        out = out.replace(k, v)
    return out


def fig_block(path: str, caption: str, label: str) -> str:
    return f"""\\begin{{figure}}[t]
    \\centering
    \\includegraphics[width=0.95\\linewidth]{{{path}}}
    \\caption{{{latex_escape(caption)}}}
    \\label{{{label}}}
\\end{{figure}}
"""


def main() -> None:
    ap = argparse.ArgumentParser(description="Write a LaTex report that includes generated experiment figures with verbose captions.")
    ap.add_argument("--art-dir", required=True)
    ap.add_argument("--output", default=None)
    args = ap.parse_args()

    art = Path(args.art_dir)
    out = Path(args.output) if args.output else art / "report.tex"

    figures = []

    # Include all generated figures in the artifact directory.
    # Prefer PDF when both PDF+PNG exist for the same stem.
    pdfs = sorted(art.glob("*.pdf"))
    pngs = sorted(art.glob("*.png"))
    stems_with_pdf = {p.stem for p in pdfs}

    selected = list(pdfs) + [p for p in pngs if p.stem not in stems_with_pdf]

    for idx, p in enumerate(selected, start=1):
        cap = (
            f"Auto-included figure '{p.name}'. "
            f"This figure was generated by the experiment pipeline; see accompanying JSON/NPZ metadata in the data directory for detailed settings (including preset a, k, and other sweep parameters where applicable)."
        )
        label = f"fig:auto:{p.stem}"
        figures.append(fig_block(p.name, cap, label))
    tex = r"""\documentclass[11pt]{article}
\usepackage[a4paper,margin=1in]{geometry}
\usepackage{graphicx}
\usepackage{float}
\usepackage{booktabs}
\usepackage{hyperref}
\title{OrderGrad Experiment Report}
\author{Auto-generated by run\_all\_experiments.sh}
\date{\today}
\begin{document}
\maketitle
\section*{Overview}
This document collects key experiment figures and provides contextual captions (including estimator settings such as the L-stat weight preset \texttt{a} where applicable).
"""
    tex += "\n".join(figures)
    tex += "\n\\end{document}\n"

    out.write_text(tex, encoding="utf-8")
    print(f"Saved: {out}")


if __name__ == "__main__":
    main()
